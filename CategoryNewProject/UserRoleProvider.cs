        public override string[] GetRolesForUser(string username)
        {
            DatabaseContext db = new DatabaseContext();
            var data = (from credential in db.Credentials
                        join userrole in db.Roles
                        on credential.RoleId equals userrole.RoleId
                        where credential.UserName == username
                        select userrole.UserRole).ToArray();
            return data;
        }

